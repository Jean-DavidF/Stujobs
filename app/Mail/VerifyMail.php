{
    use Queueable, SerializesModels;

    public $user;

    /**
     * VerifyMail constructor.
     * @param $user
     *
     * Email verify constructor
     */
    public function __construct($user)
    {
        $this->user = $user;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this
                ->subject('Stujobs : VÃ©rification de votre adresse email')
                ->view('emails.verifyUser');
    }
}
